# There are 3 conditionals in Python
# if, elif, else
# We combine conditionals with comparison operators to make decisions in our code
# Conditions are evaluated to either True or False

a = int(input("Enter the number: "))

if a == 1:
    print("The number is equal to 1")
elif a == 2: # Else If
    print("The number is equal to 2") # You can have multiple elif statements
else:
    print("The number is not equal to 1");